#include "libft.h"

long int		ft_atol(char *s)
{
	long int		ret;
	int				neg;
	unsigned int	i;

	ret = 0;
	neg = 1;
	i = 0;
	while (s[i] && s[i] >= 8 && s[i] <= 14)
		i++;
	if (s[i] == '+' || s[i] == '-')
	{
		if (s[i] == '-')
			neg = -1;
		i++;
	}
	while (s[i] && s[i] >= '0' && s[i] <= '9')
	{
		ret = ret * 10 + s[i] - '0';
		i++;
	}
	return (ret * neg);
}
